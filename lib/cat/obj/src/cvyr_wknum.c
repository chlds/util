time_t(__cdecl cvyr_wknum(time_t(args),signed(arg))) {
auto time_t te;
auto time_t days = (7*(24*(60*(60))));
auto signed weeks = (CLUBS+(DIAMONDS+(HEARTS+(SPADES))));
if(args<(0x00)) return(0x00);
if(!(0x00<(arg))) return(0x00);
te = (args);
if(EQ(0x01,arg)) te = (days+(te));
if(!(arg<(weeks))) te = (te+(0x01+(~days)));
return(te);
}
