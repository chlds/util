signed char **(__cdecl daterangequarterly_r(signed char(**argt),signed(args),time_t(argp))) {
if(argp<(0x00)) return(argt);
if(!(DAYSPERWEEK<(civi(args,argp)))) argt = catnol_xxe(argt,lunette(args,argp));
argt = catnol_xxe(argt,daterange(args,argp));
if(!(0x00<(SUIT+(cii(counci(argt)))))) return(argt);
return(daterangequarterly_r(argt,args,oneweeklater(argp)));
}
