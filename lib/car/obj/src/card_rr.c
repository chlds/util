signed char *(__cdecl card_rr(signed(argt),signed(args),signed char(*argp))) {
auto signed char *table = ("0123456789ABCDEF");
if(!argp) return(argp);
argp = cat_xe_ahead(argp,cathay(0xFF&(*(table+(argt%(args))))),(void*)(0x00));
argt = (argt/(args));
if(!argt) return(argp);
return(card_rr(argt,args,argp));
}
